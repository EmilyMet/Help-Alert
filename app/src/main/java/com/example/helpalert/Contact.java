package com.example.helpalert;

public class Contact {
    private String Name;
    private int number;


    public Contact(){

    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setAge(int number) {
        this.number = number;
    }
}
